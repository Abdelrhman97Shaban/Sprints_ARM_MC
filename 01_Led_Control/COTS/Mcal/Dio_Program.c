void  Dio_PortInit (DIO_PortConfig * PtrPortConfig)
{
	SYSCTL_RCGCGPIO_REG  |= (PtrPortConfig -> Port_Clock);                 
	while ((SYSCTL_RCGCGPIO_REG &  (PtrPortConfig -> Port_Clock)  ) == 0)   
	GPIO_PORTF_LOCK_REG  = 0x4C4F434B ;          
	GPIO_PORTF_CR_REG 	 = (PtrPortConfig -> Commit_Pins);          	
	GPIO_PORTF_PCTL_REG  = (PtrPortConfig -> Port_Ctrl);                     
	GPIO_PORTF_DIR_REG 	 = (PtrPortConfig -> Port_Dir);           
	GPIO_PORTF_AFSEL_REG = (PtrPortConfig -> Port_Mode);    
	GPIO_PORTF_DEN_REG   = 0xff;             
}

uint8 Dio_ReadPin  (DIO_PinConfig * PtrPinConfig)
{
	return READ_BIT(GPIODATA(PtrPinConfig->Port),PtrPinConfig->Pin_Number);
}

void  Dio_WritePin (DIO_PinConfig * PtrPinConfig)
{
	if ( PtrPinConfig -> Pin_Level == HIGH )
	{
		SET_BIT(GPIODATA(PtrPinConfig->Port),PtrPinConfig->Pin_Number);
	}
	else
	{
		CLR_BIT(GPIODATA(PtrPinConfig->Port),PtrPinConfig->Pin_Number);
	}
}


uint8 Dio_ReadPort (DIO_Port Port)
{
	return GPIODATA(Port);
}

void  DIO_WritePort (DIO_Port Port ,uint8 Value)
{
	GPIODATA(Port) = Value;
}

