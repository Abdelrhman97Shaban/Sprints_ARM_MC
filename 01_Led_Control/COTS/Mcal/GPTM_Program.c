void GPTM_Delay(uint16  del)
{
	SYSCTL_RCGCTIMER_REG |= 0X01;      
	TIMER0_CTL_REG = 0;      
	TIMER0_CFG_REG = 0x00;    
	TIMER0_TAMR_REG = 0X02;   
	
	TIMER0_TAILR_REG =((del *16000000)-1);  
	TIMER0_ICR_REG = 0x01;     
	
	TIMER0_IMR_REG |= (1<<0);    
	TIMER0_CTL_REG |= 0x01;      
	NVIC_EN0 |= 1<<19 ;    
}


